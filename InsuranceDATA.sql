CREATE TABLE Insurance(
InsuranceId varchar(25) NOT NULL,
Insurance_Provider varchar(25) NOT NULL,
Specialization varchar(50) NOT NULL,
PatientId varchar(25) NOT NULL,
DoctorId varchar(25) NOT NULL,
BillingAmount decimal(10,2) NOT NULL,
Insurance_Coverage decimal(10,2) NOT NULL,
Balance decimal(10,2) NOT NULL,
PRIMARY KEY (InsuranceId),
FOREIGN KEY (PatientID) REFERENCES Patient(PatientID),
FOREIGN KEY (DoctorID) REFERENCES Doctor(DoctorID)
);

